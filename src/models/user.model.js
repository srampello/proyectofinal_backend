import mongoose from "mongoose";

const userCollection = 'user'

const userSchema = new mongoose.Schema({
    username: {
        type: String,
        require: true,
        trim: true
    },
    email: {
        type: String,
        require: true,
        trim: true,
        unique: true
    },
    password: {
        type: String,
        require: true,
    }
})

export default mongoose.model(userCollection, userSchema)